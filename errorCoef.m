% Αυτή η συνάρτηση υπολογίζει το μέγιστο σφάλμα μεταξύ δυο στοιχείων από
% τον πίνακα συντελεστών
% C: πίνακας συντελεστών NxN, Z: πίνακας συντελεστών NxN
% err: linf-νόρμα σφάλματος μεταξύ των διανυσματοποιημένων C και Z
function err = errorCoef(Z,C)

err = sum(sum( abs(Z-C) )) / (size(C,1)*size(C,2));